package model;

public class Location
{
	private String location;
	
	/**
	 * Constructor
	 * @param location
	 */
	public Location(String location)
	{
		this.location = location;
	}
	/**
	 * @return location
	 */
	public String getLocation()
	{
		return location;
	}
	/**
	 * @param set location
	 */
	public void setLocation(String location)
	{
		this.location = location;
	}

}
